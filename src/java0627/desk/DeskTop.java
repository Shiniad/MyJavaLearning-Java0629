package java0627.desk;

public interface DeskTop {
	
	/**
	 * @return 桌面的面积
	 */
	public double getArea();
	
	/**
	 * @return 桌腿的厚度
	 */
	public double getThicksNess();
	
	/**
	 * @return 桌腿接口的大小
	 */
	public double getDiagonalLineLen();
	
	/**
	 * @return 桌面的品种
	 */
	public String getStrain();
	
	/**
	 * @return 桌面的材质
	 */
	public String getMaterial();
}
